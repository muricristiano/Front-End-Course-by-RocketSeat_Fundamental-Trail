/* 
    Operadores de comparação - Boolean
*/

let x = 1
let y = 2

// == igual a
console.log("igual a");
console.log(x == y);
console.log(x == 1);
console.log(x == "1")
// tanto string quanto number ele irá considerar o mesmo valor

// !- diferente de
console.log("diferente de");
console.log(x != y);
console.log(x != 1);
console.log(x != "1"); 
// tanto string quanto number ele irá considerar o mesmo valor
