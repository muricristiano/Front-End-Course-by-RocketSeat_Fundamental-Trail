// Operadores de atribuição (Assignment)

// x += 5 é o mesmo que x = x + 5 (e todos abaixo seguem a mesma ideia)


let x

console.log(x)


// assignment 
x = 10 
console.log(x)

// addition assignment
x += 5 // mesmo que x = x + 5 (e todos abaixo seguem a mesma ideia)
console.log(x)

// subtraction assingment
x -= 1
console.log(x)

// multiplication assignment
x *= 2
console.log(x)

// division assignment
x /= 4
console.log(x)

// remainder assignment
x %= 5
console.log(x);

// elevation assignment
x **= 3
console.log(x);

